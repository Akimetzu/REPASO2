        modelCombo = new DefaultComboBoxModel();
        cboCategoria.setModel(modelCombo);
        fila = tbdatos.getSelectedRow();
        Categoria cat = categoriDao.read(Integer.parseInt(tbdatos.getValueAt(fila, 1).toString()));
