    private void cargarCombo(){
        modelCombo = new DefaultComboBoxModel();
        cboCategoria.setModel(modelCombo);
        List<Categoria> lista1 = categoriDao.readAll();
        Categoria cat  = new Categoria();
        cboCategoria.removeAllItems();
        modelCombo.addElement(new Categoria(0,"Seleccionar Categoría",0));
        for(int i=0;i<lista1.size();i++){
            modelCombo.addElement(new Categoria(lista1.get(i).getIdcategoria(),lista1.get(i).getNombre(),lista1.get(i).getEstado()));
        }
    }
    private void listarProducto(){
        model = (DefaultTableModel)tbdatos.getModel();
        List<Map<String, Object>> lista2 = productoDao.readAll2();
        Object[] datos = new Object[7];
        int x = 1;
        for (Map row : lista2) {
              datos[0]=x;
              datos[1]=row.get("idcategoria");
              datos[2]=row.get("categoria");
              datos[3]=row.get("idproducto");
              datos[4]=row.get("producto");
              datos[5]=row.get("precio");
              datos[6]=row.get("cantidad");
              model.addRow(datos); 
              x++;
        }
        tbdatos.setModel(model);
    
    }
     private void limpiar_table(){
        for(int i=0; i<tbdatos.getRowCount();i++){
            model.removeRow(i);
            i=-1;
        }    
    }
    private void limpiarFormulario(){
        cboCategoria.setSelectedIndex(0);
        txtproducto.setText("");
        txtprecio.setText("");
        txtCantidad.setText("");
        idcat=0;
        idprod=0;
    }
